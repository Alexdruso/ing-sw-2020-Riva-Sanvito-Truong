package it.polimi.ingsw.client.clientstates;

/**
 * The client states.
 */
public enum ClientState {
    /**
     * Connect to server client state.
     */
    CONNECT_TO_SERVER,
    /**
     * Set nickname client state.
     */
    SET_NICKNAME,
    /**
     * Join lobby client state.
     */
    JOIN_LOBBY,
    /**
     * Disconnect client state.
     */
    DISCONNECT,
    ;
}
